package module_3_4.files;

public class File {

    private String name;
    private int size;

    // getters & setters

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
